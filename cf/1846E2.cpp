using namespace output;

bool solve() {
    ll n;
	cin >> n;
	for (int st = 3;st < 65;st++) {
		ll l = 2, r = pow(n, 1./(st-1));
		while (l <= r) {
			ll mi = (l+r)/2;
			
			ll cur = 1, sm = 0;
			for (int i = 0;i < st;i++) {
				sm += cur;
				if (i != st-1) cur *= mi;
			}

			if (sm == n) return true;

			if (sm < n) {
				l = mi + 1;
			} else {
				r = mi - 1;
			}
		}
	}
	return false;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) cout << (solve() ? "YES" : "NO") << endl;
    return 0;
}