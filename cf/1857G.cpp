ll pow(ll a, ll b, ll m){
    ll ans = 1;
    while(b){
        if (b&1) ans = (ans*a) % m;
        b /= 2;
        a = (a*a) % m;
    }
    return ans;
}

void solve() {
    int n, s;
	cin >> n >> s;
	vvi edges;
	for (int i = 0;i < n-1;i++) {
		int a, b, c;
		cin >> a >> b >> c;
		edges.push_back({a, b, c});
	}
	sort(edges.begin(), edges.end(), [](vi &a, vi &b) {
		return a[2] < b[2];
	});

	dsu d(n);
	ll ret = 1;
	ll MOD = 998244353;

	for (auto v : edges) {
		int a = v[0];
		int b = v[1];
		int c = v[2];
		a--;
		b--;
		ret *= pow((ll)(s - c + 1), ((ll)d.size(a) * d.size(b) - 1), MOD);
		ret %= MOD;
		d.merge(a, b);
	}
	cout << ret << endl;
}

